fun main() {
    // Boolean
    val t = true
    val f = false

    // Negation
    println(t.not())
    println(!f)

    // Xor
    println(t.xor(f))
}

/****************
 * Output:      *
 * false        *
 * true         *
 * true         *
 ****************/

