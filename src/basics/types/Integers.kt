fun main() {
    // Integer
    val one = 1
    val two = 2
    val oneMillion = 1_000_000

    // Operators
    println(one + two)
    println(oneMillion - one)
    println(two * oneMillion)
    println(oneMillion / two)
}

/****************
 * Output:      *
 * 3            *
 * 999999       *
 * 2000000      *
 * 500000       *
