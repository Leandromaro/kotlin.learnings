package docs.concepts.controlFlow.exceptions

/***********************
 * Kotlin Learnings    *
 ***********************
 * Try, catch, finally *
 ***********************/

/**
 * Try, catch, and finally work as you know
 * them from most other programming languages.
 */
fun main() {
    try {
        // Try something.
    } catch (e: Exception) {
        // Zero or more catch blocks
        // for different exceptions.
    } finally {
        // finally is optional. The code
        // here is always executed.
    }
}
