fun main() {
    // Double
    val x = 1.25
    val y = 5.0
    val z = 1_000_000.0

    // Operators
    println(x + y)
    println(y - x)
    println(z * x)
    println(z / y)
}

/****************
 * Output:      *
 * 6.25         *
 * 3.75         *
 * 1250000.0    *
 * 200000.0     *
